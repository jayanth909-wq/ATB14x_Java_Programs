package ex_014__Operators_BrushUp;

public class Lab08_All_Operators {
    public static void main(String[] args) {

        int g = 10;
        int b = 20;

        System.out.println(g>b);
        System.out.println(g<b);
        System.out.println(g>=b);
        System.out.println(g==b);
        System.out.println(g!=b);


        //Compound operators


        int hub = 10;
        hub += 20;
        System.out.println(hub);

        int good = 33;
        good *= 21;
        System.out.println(good);

        int sweet = 43;
        sweet -= 3;
        System.out.println(sweet);


    }
}
