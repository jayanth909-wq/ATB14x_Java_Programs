package ex_10_Switch;

public class Lab106_Switch_04 {
    public static void main(String[] args) {
        char c = 'A';
        switch (c){
            case 1:
                System.out.println("ASCII");
                break;
            case 2:
                System.out.println("Not");
                break;
            default:
                System.out.println("no match");



        }
    }
}
