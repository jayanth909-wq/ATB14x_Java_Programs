package ex_10_Switch;

public class Lab119_Switch {
    public static void main(String[] args) {
        int a = 7;
        switch (7){
            case 1:
                System.out.println("valid");
                break;
            case -1:
                System.out.println("not valid");
                break;
            case 9:
                System.out.println("9");
                break;
            default:
                System.out.println("not realted");
        }
    }
}
