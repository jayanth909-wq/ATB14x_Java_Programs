package ex_04_Operators;

public class Lab037_Arthematic_Operators {
    public static void main(String[] args) {
        int a = 23;
        int b = 54;
        float c = 3.14f;
// Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder
        System.out.println(a-b);
        System.out.println(a+b);
        System.out.println(a/b);
        System.out.println(a*b);
        System.out.println(c*a/b);
        System.out.println("a-b");

    }
}
