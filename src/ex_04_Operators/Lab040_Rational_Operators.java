package ex_04_Operators;

public class Lab040_Rational_Operators
{
    public static void main(String[] args) {
        // < Less Than
        // < =  -> Less than or equal to
        // > Greater
        // > = Greater or equal
        // == ->  Equal to (but checking)
        // != -> Not equal

        int a = 78;
        int b = 86;
        boolean c = a > b;
        System.out.println(c);

        int punya_age = 26;
        int rahul_age = 28;
        boolean actual_age = punya_age == rahul_age;
        System.out.println(actual_age);

    }
}
