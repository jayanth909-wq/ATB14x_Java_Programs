package ex_09_Switch_Statements_No_Boolean;

public class Lab10 {
    public static void main(String[] args) {
        char bv = 'd';
        switch (bv){
            default:
                System.out.println("ok");
            case 2:
                System.out.println('A');
                break;
            case 3:
                System.out.println("98");

        }
    }
}
